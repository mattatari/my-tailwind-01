
/**
 * 
 * @param {*} a 
 * @param {*} b 
 * @returns a + b
 */
let sum = (a, b) => {
    return a + b;
}

// -----------------------
/**
 * 
 * @param {*} a 
 * @param {*} b 
 * @returns a - b
 */
let subtract = (a, b) => {
    return a - b;
}

// -----------------------
module.exports = {
    sum,
    subtract
}